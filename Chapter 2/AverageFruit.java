public class AverageFruit {
	public static void main(String[] args) {
	// declare and initialize three variables
	double numOranges = 50.E-1;    // initial value is 5
	double numApples = 1.0E1;      // initial value is 10.0
	double averageFruit = 0.0;
	
	averageFruit = (numOranges + numApples) / 2.0;
	System.out.println("A totally fruitly program");
	System.out.println("The average fruit is " + averageFruit);
	}
}